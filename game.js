function showScreen(name) {
  Object.values(screens).forEach(s => s.classList.remove('active'));
  screens[name].classList.add('active');
}

/* ── Leaderboard (localStorage) ── */
const LB_KEY = 'orn_v1_lb';

function getLB() {
  try { return JSON.parse(localStorage.getItem(LB_KEY) || '[]'); }
  catch { return []; }
}

function saveLB(name, uid, pts) {
  const lb = getLB();
  const i  = lb.findIndex(x => x.uid === uid);
  if (i >= 0) { if (lb[i].score < pts) lb[i].score = pts; }
  else lb.push({ name, uid, score: pts });
  lb.sort((a, b) => b.score - a.score);
  localStorage.setItem(LB_KEY, JSON.stringify(lb.slice(0, 50)));
}

function renderLB() {
  const lb = getLB();
  const el = $('lb-list');
  if (!lb.length) {
    el.innerHTML = '<div class="lb-empty">Пока нет результатов.<br>Сыграй первым!</div>';
    return;
  }
  const medals = ['🥇','🥈','🥉'];
  el.innerHTML = lb.slice(0, 10).map((item, i) => {
    const you = item.uid === USER_ID;
    const rank = medals[i] || (i + 1);
    return `<div class="lb-item">
      <span class="lb-rank">${rank}</span>
      <span class="lb-name${you ? ' you' : ''}">${esc(item.name)}${you ? ' (ты)' : ''}</span>
      <span class="lb-score">${item.score}</span>
    </div>`;
  }).join('');
}

function esc(s) {
  return String(s).replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;');
}

/* ── Game logic ── */
function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function startGame() {
  const real = shuffle(BILLS.filter(b => b.type === 'real')).slice(0, 5);
  const fake = shuffle(BILLS.filter(b => b.type === 'fake')).slice(0, 5);
  questions = shuffle([...real, ...fake]);
  qIndex = 0; score = 0; correct = 0;
  showScreen('game');
  renderQuestion();
}

function renderQuestion() {
  answered = false;
  const q = questions[qIndex];

  $('q-num').textContent       = qIndex + 1;
  $('score').textContent       = score;
  $('progress-fill').style.width = `${(qIndex / 10) * 100}%`;
  $('bill-title').textContent  = q.title;
  $('bill-desc').textContent   = q.desc;

  $('answer-reveal').classList.add('hidden');
  $('answer-buttons').style.display = 'flex';

  const bReal = $('btn-real'), bFake = $('btn-fake');
  bReal.className = 'btn-ans btn-real';
  bFake.className = 'btn-ans btn-fake';
  bReal.disabled  = false;
  bFake.disabled  = false;

  startTimer();
}

function startTimer() {
  timeLeft = 15;
  clearInterval(timerID);
  updateTimer();
  timerID = setInterval(() => {
    timeLeft--;
    updateTimer();
    if (timeLeft <= 0) { clearInterval(timerID); onTimeout(); }
  }, 1000);
}

function updateTimer() {
  const pct  = (timeLeft / 15) * 100;
  const fill = $('timer-fill');
  fill.style.width = pct + '%';
  $('timer-text').textContent = timeLeft;
  fill.classList.toggle('warn',   timeLeft <= 9 && timeLeft > 4);
  fill.classList.toggle('danger', timeLeft <= 4);
}

function onTimeout() {
  if (answered) return;
  answered = true;
  disableAnswerBtns();
  showReveal('timeout', 0, questions[qIndex]);
}

function onAnswer(choice) {
  if (answered) return;
  answered = true;
  clearInterval(timerID);

  const q       = questions[qIndex];
  const isRight = q.type === choice;
  const bonus   = timeLeft >= 10 ? 5 : 0;
  const pts     = isRight ? (10 + bonus) : 0;

  if (isRight) { score += pts; correct++; }
  $('score').textContent = score;

  const bReal = $('btn-real'), bFake = $('btn-fake');
  disableAnswerBtns();

  const correctBtn = q.type === 'real' ? bReal : bFake;
  const wrongBtn   = q.type === 'real' ? bFake : bReal;
  correctBtn.classList.add('correct');
  if (!isRight) wrongBtn.classList.add('wrong');

  showReveal(isRight ? 'correct' : 'wrong', pts, q, bonus);
}

function disableAnswerBtns() {
  $('btn-real').disabled = true;
  $('btn-fake').disabled = true;
  $('answer-buttons').style.display = 'none';
}

function showReveal(state, pts, q, bonus = 0) {
  const verdictEl = $('answer-verdict');
  const explEl    = $('answer-explanation');

  verdictEl.className = 'answer-verdict ' + state;

  if (state === 'correct') {
    verdictEl.textContent = bonus
      ? `✅ Правильно! +${pts} (включая +${bonus} за скорость)`
      : `✅ Правильно! +${pts}`;
  } else if (state === 'wrong') {
    verdictEl.textContent = '❌ Неправильно';
  } else {
    verdictEl.textContent = '⏱ Время вышло!';
  }

  let txt = (q.type === 'real' ? '🏛 ' : '🤖 ') + q.explanation;
  if (q.type === 'real' && q.meta) txt += '\n\n👤 ' + q.meta;
  explEl.textContent = txt;

  $('answer-reveal').classList.remove('hidden');
}

function nextQuestion() {
  qIndex++;
  if (qIndex >= 10) showResult();
  else renderQuestion();
}

