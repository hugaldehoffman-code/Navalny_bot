function shareResult() {
  const text = `Я набрал ${score} очков в игре «Реальный или нейросеть?»! ${correct}/10 правильных.\n@navalniy_chat_bot`;
  if (tg?.switchInlineQuery !== undefined) {
    tg.sendData(JSON.stringify({ action: 'share_score', score, correct }));
  } else if (navigator.share) {
    navigator.share({ text }).catch(() => {});
  } else {
    navigator.clipboard?.writeText(text).then(() => alert('Скопировано!'));
  }
}

